#include "libft.h"

long long	ft_atoll(const char *str)
{
	long long	num;
	long long	zrs;
	long long	i;

	num = 0;
	zrs = 1;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == ' '))
		i++;
	if (str[i] == '-')
	{
		zrs = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (ft_isdigit(str[i]))
	{
		num = (num * 10) + (str[i] - '0');
		i++;
	}
	num *= zrs;
	return (num);
}
